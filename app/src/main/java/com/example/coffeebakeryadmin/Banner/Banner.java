package com.example.coffeebakeryadmin.Banner;

public class Banner {
    String ten;
    String link;

    public Banner() {
    }

    public Banner(String ten, String link) {
        this.ten = ten;
        this.link = link;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

}
